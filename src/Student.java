public class Student {
    private String name = "Abylay";
    private int id = 26;
    private int yearOfStudy = 2;

    public Student(String name, int id) {
        this.name = name;
        this.id = id;
        this.yearOfStudy = 1;
    }

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }

    public int getYearOfStudy() {
        return yearOfStudy ;


    }

}
